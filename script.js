// Function to parse the questions from the provided text format
function parseQuestions(text) {
    const questionsData = [];
    const rawQuestions = text.split('+++++');

    rawQuestions.forEach(rawQ => {
        if (rawQ.trim()) {
            const parts = rawQ.trim().split('====');
            if (parts.length > 1) {
                const questionText = parts[0].trim();
                const answers = parts.slice(1).filter(ans => ans.trim());

                let correctAnswer = null;
                const cleanAnswers = [];

                answers.forEach(ans => {
                    if (ans.startsWith('#')) {
                        correctAnswer = ans.substring(1).trim();
                        cleanAnswers.push(ans.substring(1).trim());
                    } else {
                        cleanAnswers.push(ans.trim());
                    }
                });

                if (correctAnswer && cleanAnswers.length > 0) {
                    questionsData.push({
                        question: questionText,
                        options: cleanAnswers,
                        correct_answer: correctAnswer
                    });
                }
            }
        }
    });
    return questionsData;
}

