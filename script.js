const questions = parseQuestions(rawQuizData);
let shuffledQuestions = [];
let currentQuestionIndex = 0;
let score = 0;
let selectedOption = null;

const questionNumberElement = document.getElementById('question-number');
const currentQElement = document.getElementById('current-q');
const totalQElement = document.getElementById('total-q');
const questionTextElement = document.getElementById('question-text');
const optionsContainer = document.getElementById('options-container');
const feedbackElement = document.getElementById('feedback');
const checkAnswerBtn = document.getElementById('check-answer-btn');
const nextQuestionBtn = document.getElementById('next-question-btn');
const restartQuizBtn = document.getElementById('restart-quiz-btn');
const scoreDisplay = document.getElementById('score-display');

function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
}

function startQuiz() {
    shuffledQuestions = [...questions];
    shuffleArray(shuffledQuestions); // Shuffle the order of questions
    shuffledQuestions.forEach(q => shuffleArray(q.options)); // Shuffle options for each question
    currentQuestionIndex = 0;
    score = 0;
    scoreDisplay.textContent = '';
    restartQuizBtn.style.display = 'none';
    loadQuestion();
}

function loadQuestion() {
    selectedOption = null;
    feedbackElement.textContent = '';
    checkAnswerBtn.style.display = 'block';
    nextQuestionBtn.style.display = 'none';

    if (currentQuestionIndex < shuffledQuestions.length) {
        const currentQuestion = shuffledQuestions[currentQuestionIndex];
        currentQElement.textContent = currentQuestionIndex + 1;
        totalQElement.textContent = shuffledQuestions.length;
        questionTextElement.textContent = currentQuestion.question;
        optionsContainer.innerHTML = ''; // Clear previous options

        currentQuestion.options.forEach(option => {
            const button = document.createElement('button');
            button.classList.add('option-button');
            button.textContent = option;
            button.addEventListener('click', () => selectOption(button, option));
            optionsContainer.appendChild(button);
        });
    } else {
        showResults();
    }
}

function selectOption(button, optionValue) {
    // Deselect any previously selected option
    const currentSelected = document.querySelector('.option-button.selected');
    if (currentSelected) {
        currentSelected.classList.remove('selected');
    }
    // Select the new option
    button.classList.add('selected');
    selectedOption = optionValue;
}

function checkAnswer() {
    if (selectedOption === null) {
        feedbackElement.textContent = 'Пожалуйста, выберите ответ.';
        feedbackElement.style.color = 'orange';
        return;
    }

    const currentQuestion = shuffledQuestions[currentQuestionIndex];
    const optionButtons = optionsContainer.children;
    let isCorrect = false;

    for (let i = 0; i < optionButtons.length; i++) {
        const button = optionButtons[i];
        const optionText = button.textContent;

        // Reset previous feedback classes
        button.classList.remove('correct', 'incorrect', 'selected');

        if (optionText === currentQuestion.correct_answer) {
            button.classList.add('correct'); // Highlight correct answer in green
        }

        if (optionText === selectedOption) {
            if (selectedOption === currentQuestion.correct_answer) {
                // If the selected is correct, it will already be green
                isCorrect = true;
            } else {
                button.classList.add('incorrect'); // Highlight incorrect selected answer in red
            }
        }
    }

    if (isCorrect) {
        score++;
        feedbackElement.textContent = 'Правильно!';
        feedbackElement.style.color = 'green';
    } else {
        feedbackElement.textContent = 'Неправильно. Правильный ответ выделен зеленым.';
        feedbackElement.style.color = 'red';
    }

    // Disable all options after checking
    Array.from(optionButtons).forEach(button => button.removeEventListener('click', () => {}));
    Array.from(optionButtons).forEach(button => button.style.pointerEvents = 'none');


    checkAnswerBtn.style.display = 'none';
    nextQuestionBtn.style.display = 'block';
    updateScoreDisplay();
}

function nextQuestion() {
    currentQuestionIndex++;
    if (currentQuestionIndex < shuffledQuestions.length) {
        loadQuestion();
    } else {
        showResults();
    }
}

function showResults() {
    questionTextElement.textContent = 'Тест завершен!';
    optionsContainer.innerHTML = '';
    feedbackElement.textContent = '';
    checkAnswerBtn.style.display = 'none';
    nextQuestionBtn.style.display = 'none';
    restartQuizBtn.style.display = 'block';
    scoreDisplay.textContent = `Вы ответили правильно на ${score} из ${shuffledQuestions.length} вопросов.`;
}

function updateScoreDisplay() {
    scoreDisplay.textContent = `Правильных ответов: ${score} из ${currentQuestionIndex + 1}`;
}


checkAnswerBtn.addEventListener('click', checkAnswer);
nextQuestionBtn.addEventListener('click', nextQuestion);
restartQuizBtn.addEventListener('click', startQuiz);

// Initial start of the quiz
startQuiz();